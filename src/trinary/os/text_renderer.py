DEFAULT_BITMAP = [0x00, 0x42, 0x24, 0x18, 0x18, 0x24, 0x42, 0x00]


class TextRenderer:
    def __init__(self, framebuffer):
        self.fb = framebuffer
        self.cursor_x = 0
        self.cursor_y = 0
        self.color = 2

    def clear(self, color=0):
        self.fb.clear(color)
        self.cursor_x = 0
        self.cursor_y = 0

    def set_color(self, color):
        self.color = color

    def put_char(self, ch, color=None, x=None, y=None):
        c = color if color is not None else self.color
        cx = x if x is not None else self.cursor_x
        cy = y if y is not None else self.cursor_y
        if cx < 0 or cx >= 8 or cy < 0 or cy >= 8:
            return
        bitmap = FONT_8x8.get(ch.upper(), DEFAULT_BITMAP)
        px = cx * 8
        py = cy * 8
        for row in range(8):
            mask = bitmap[row] if row < len(bitmap) else 0
            for col in range(8):
                if mask & (0x80 >> col):
                    self.fb.set_pixel(px + col, py + row, c)

    def print_at(self, text, x, y, color=None):
        for i, ch in enumerate(text):
            self.put_char(ch, color=color, x=x + i, y=y)

    def scroll(self):
        buf = self.fb.get_buffer()
        for y in range(8, 64):
            for x in range(64):
                buf[y - 8][x] = buf[y][x]
        for y in range(56, 64):
            for x in range(64):
                buf[y][x] = 0

    def newline(self):
        self.cursor_x = 0
        self.cursor_y += 1
        if self.cursor_y >= 8:
            self.scroll()
            self.cursor_y = 7

    def write(self, text, color=None):
        for ch in str(text):
            if ch == "\n":
                self.newline()
            else:
                self.put_char(ch, color=color)
                self.cursor_x += 1
                if self.cursor_x >= 8:
                    self.newline()

    def writeln(self, text, color=None):
        self.write(text, color=color)
        self.newline()

    def draw_cursor(self, visible=True):
        buf = self.fb.get_buffer()
        px = self.cursor_x * 8
        py = self.cursor_y * 8
        for col in range(8):
            if visible:
                buf[py + 7][px + col] = 2
            else:
                if buf[py + 7][px + col] == 2:
                    buf[py + 7][px + col] = 0
